def ecological_ode(NDVI, SoilMoist):
    # This is the math the ML found
    term1 = (0.6194 * SoilMoist) / (NDVI + 0.1389)
    dNDVI_dt = NDVI + term1 - 0.6079
    return dNDVI_dt

def rk4_step(v, s, dt):
    k1 = ecological_ode(v, s)
    k2 = ecological_ode(v + k1*dt/2, s)
    k3 = ecological_ode(v + k2*dt/2, s)
    k4 = ecological_ode(v + k3*dt, s)
    return v + (dt/6) * (k1 + 2*k2 + 2*k3 + k4)


