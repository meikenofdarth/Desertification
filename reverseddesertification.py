def fetch_data_failsafe(roi, start_date, end_date):
    date_list = ee.List.sequence(0, ee.Date(end_date).difference(ee.Date(start_date), 'month').subtract(1))
    start_ee = ee.Date(start_date)

    def process_month(n):
        m_start = start_ee.advance(n, 'month')
        m_end = m_start.advance(1, 'month')
        feature_dict = {'date': m_start.format('YYYY-MM-dd')}

        for name, info in vars_map.items():
            col = ee.ImageCollection(info['id']).filterDate(m_start, m_end).select(info['band'])

            # Using median for robustness against clouds
            img = col.median()

            # Performing reduction
            stats = img.reduceRegion(
                reducer=ee.Reducer.mean(),
                geometry=roi,
                scale=info['res'],
                maxPixels=1e9
            )

            # SAFE GET: Check if the key exists in the dictionary, otherwise return -9999
            val = ee.Dictionary(stats).get(info['band'], -9999)
            feature_dict[name] = val

        return ee.Feature(None, feature_dict)

    print("Requesting server-side computation (Failsafe Mode)...")
    raw_output = ee.FeatureCollection(date_list.map(process_month)).getInfo()
    features = raw_output['features']

    df = pd.DataFrame([f['properties'] for f in features])
    df['date'] = pd.to_datetime(df['date'])
    df = df.set_index('date').replace(-9999, np.nan)
    df = df.apply(pd.to_numeric, errors='coerce')

    # Scientific Scaling & Feature Engineering
    if 'NDVI' in df: df['NDVI'] *= 0.0001
    if 'EVI' in df: df['EVI'] *= 0.0001
    if 'LAI' in df: df['LAI'] *= 0.1
    if 'Albedo' in df: df['Albedo'] *= 0.001
    if 'LST_Day' in df:
        df['LST_Day'] *= 0.02
        df['LST_Night'] *= 0.02
        df['Temp_Delta'] = df['LST_Day'] - df['LST_Night']
    if 'Evapo' in df: df['Evapo'] *= 0.1

    # Handle gaps with interpolation
    df = df.interpolate(method='linear').ffill().bfill()
    return df


