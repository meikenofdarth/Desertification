def calculate_lyapunov(v_start, s, iterations=1000, dt=0.01):
    v = v_start
    v_perturbed = v_start + 1e-6

    lyapunov_sum = 0

    for i in range(iterations):
        v_next = rk4_step(v, s, dt)
        v_p_next = rk4_step(v_perturbed, s, dt)

        # Calculating divergence
        distance = np.abs(v_next - v_p_next)
        lyapunov_sum += np.log(distance / 1e-6)

        # Rescale the perturbation
        v = v_next
        v_perturbed = v_next + (v_p_next - v_next) * (1e-6 / distance)

    return lyapunov_sum / (iterations * dt)

